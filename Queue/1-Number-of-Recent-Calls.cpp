class RecentCounter
{
    vector<int> records;
    int st;

public:
    RecentCounter() : st(0) {}

    int ping(int t)
    {
        records.push_back(t);
        while (records[st] < t - 3000)
            st++;
        return records.size() - st;
    }
};

